"""行业匹配分析"""
import akshare as ak

df = ak.sw_index_second_info()

# 目标行业关键词搜索
keywords = ['航空', '养殖', '畜牧', '化工', '煤炭', '有色', '航运', '港口',
            '酒店', '旅游', '半导体', '光伏', '工程', '机械', '钢铁', '铜', '铝']

print('目标行业与申万行业匹配情况:')
print('=' * 60)

found = {}
for kw in keywords:
    matches = df[df['行业名称'].str.contains(kw)]
    if len(matches) > 0:
        found[kw] = matches[['行业代码', '行业名称', '静态市盈率', 'TTM(滚动)市盈率', '市净率']].to_dict('records')

for kw, records in found.items():
    print(f"\n【{kw}】匹配到 {len(records)} 个:")
    for r in records:
        print(f"  {r['行业代码']} | {r['行业名称']} | PE:{r['静态市盈率']} | PB:{r['市净率']}")

print("\n" + "=" * 60)
print("\n【建议的行业映射】:")
